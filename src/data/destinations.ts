export function getDestinationById(id: string): Destination | undefined {
  return destinations.find(d => d.id === id)
}

export function getDestinationsByState(stateId: string): Destination[] {
  return destinations.filter(d => d.stateId === stateId)
}

export function getDestinationsByTag(tag: string): Destination[] {
  return destinations.filter(d => d.tags.includes(tag as any))
}

export function getFeaturedDestinations(): Destination[] {
  return [
    destinations.find(d => d.id === 'miami')!,
    destinations.find(d => d.id === 'las-vegas')!,
    destinations.find(d => d.id === 'nashville')!,
    destinations.find(d => d.id === 'nyc')!,
    destinations.find(d => d.id === 'los-angeles')!,
    destinations.find(d => d.id === 'scottsdale')!,
  ]
}

export function getTrendingPartyDestinations(): Destination[] {
  return destinations.filter(d => d.tags.includes('party') || d.nightlifeRating >= 4).slice(0, 6)
}

export function getLuxuryDestinations(): Destination[] {
  return destinations.filter(d => d.tags.includes('luxury')).slice(0, 6)
}

export function getRelaxingDestinations(): Destination[] {
  return destinations.filter(d => d.tags.includes('relaxing')).slice(0, 6)
}

export function searchDestinations(query: string): Destination[] {
  const q = query.toLowerCase()
  return destinations.filter(d =>
    d.name.toLowerCase().includes(q) ||
    d.description.toLowerCase().includes(q) ||
    d.tags.some(t => t.includes(q))
  )
}
