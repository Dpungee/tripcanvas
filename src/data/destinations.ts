// ─── HELPER FUNCTIONS ─────────────────────────────────────────────────

export function getDestinationById(id: string) {
  return destinations.find((d) => d.id === id);
}

export function getDestinationsByState(stateId: string) {
  return destinations.filter((d) => d.stateId === stateId);
}

export function getDestinationsByTag(tag: string) {
  return destinations.filter((d) => d.tags.includes(tag));
}

export function getFeaturedDestinations() {
  return destinations.filter((d) => d.tags.includes('featured'));
}

export function getTrendingPartyDestinations() {
  return destinations.filter((d) => d.tags.includes('party')).slice(0, 6);
}

export function getLuxuryDestinations() {
  return destinations.filter((d) => d.tags.includes('luxury')).slice(0, 6);
}

export function getRelaxingDestinations() {
  return destinations.filter((d) => d.tags.includes('relaxing')).slice(0, 6);
}

export function searchDestinations(query: string) {
  const q = query.toLowerCase();
  return destinations.filter(
    (d) =>
      d.name.toLowerCase().includes(q) ||
      d.stateId.toLowerCase().includes(q) ||
      d.tags.some((t: string) => t.includes(q)) ||
      d.description.toLowerCase().includes(q)
  );
}
